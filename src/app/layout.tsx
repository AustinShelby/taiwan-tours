import "./globals.css";
import { Inter, Plus_Jakarta_Sans } from "next/font/google";
import {
  storyblokInit,
  apiPlugin,
  getStoryblokApi,
} from "@storyblok/react/rsc";
import { StoryblokProvider } from "@/components/StoryblokProvider";
import Link from "next/link";
import { curryFetch } from "@/utils/curryFetch";

const inter = Inter({ subsets: ["latin"], variable: "--font-inter" });

const plus_jakarta_sans = Plus_Jakarta_Sans({
  subsets: ["latin"],
  weight: ["500", "700", "800"],
  variable: "--font-jakarta",
});

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

storyblokInit({
  accessToken: process.env.STORYBLOK_TOKEN,
  use: [apiPlugin],
  apiOptions: {
    fetch: curryFetch,
  },
});

const fetchSiteData = async () => {
  const client = getStoryblokApi();
  const response = await client.getStory("site-data", {
    version: "draft",
    resolve_relations: "tour_list.tours",
  });
  return response.data.story;
};

const RootLayout = async ({ children }: { children: React.ReactNode }) => {
  const story = await fetchSiteData();
  return (
    <>
      <StoryblokProvider>
        <html
          lang="en"
          className={`${inter.variable} ${plus_jakarta_sans.variable}`}
        >
          <body className="font-sans">
            <header className="bg-main">
              <nav className="component py-8 flex justify-between items-baseline">
                <Link href={"/"}>Home</Link>
                <Link href={"/tours"}>Browse Tours</Link>
              </nav>
            </header>
            {children}
            <footer className="bg-secondary">
              <div className="component py-4">
                <p className="opacity-80">{story.content.footer}</p>
              </div>
            </footer>
          </body>
        </html>
      </StoryblokProvider>
    </>
  );
};

export default RootLayout;
